<!-- LICENCIAS -->
<h2 class="adv-section-title">📄 Licencias de Marca</h2>

<div class="adv-card">
    <div class="adv-card-title">💡 ¿Cuándo conviene licenciar en lugar de franquiciar?</div>
    <p>La licencia es más simple que la franquicia: se otorga el derecho de uso de la marca (y/o tecnología) a un tercero, sin transferir necesariamente el sistema completo de operación. Es ideal para:</p>
    <ul>
        <li>Merchandising y productos con la marca</li>
        <li>Expansión en sectores adyacentes al negocio principal</li>
        <li>Ingreso a mercados extranjeros con bajo riesgo</li>
        <li>Monetización de la marca en canales digitales</li>
    </ul>
    <p style="margin-top:10px;">El contrato de licencia debe definir claramente: territorio, exclusividad, plazo, canon, estándares de calidad y causales de resolución.</p>
</div>

<!-- AGENCIAS Y DISTRIBUCIÓN -->
<h2 class="adv-section-title">🤝 Agencias y Representaciones</h2>

<div class="adv-card">
    <div class="adv-card-title">📍 Modelos de expansión mediante agentes</div>
    <p>Las agencias y representaciones permiten ampliar el alcance de la marca con menor inversión y riesgo que la expansión propia:</p>
    <ul>
        <li><strong>Agente comercial:</strong> actúa en nombre de la marca en un territorio definido, a comisión.</li>
        <li><strong>Distribuidor exclusivo:</strong> compra y revende en nombre propio. Requiere definir territorialidad, precios mínimos y estándares de presentación.</li>
        <li><strong>Representante autorizado:</strong> ofrece productos/servicios con respaldo de la marca pero bajo su propia estructura.</li>
    </ul>
    <p>Todos estos contratos deben estar por escrito con cláusulas de protección de la marca, confidencialidad y causales claras de rescisión.</p>
</div>

<!-- VALORACIÓN -->
<h2 class="adv-section-title">📈 Valoración de la Marca</h2>

<div class="adv-card">
    <div class="adv-card-title">🔢 Métodos de valuación de intangibles</div>
    <p>La valoración de la marca es necesaria para operaciones de M&A, ingreso de socios, franquicias y declaraciones fiscales:</p>
    <ul>
        <li><strong>Método de ingresos (DCF):</strong> valor presente de los flujos futuros atribuibles a la marca.</li>
        <li><strong>Método de costo:</strong> costo de reproducción o reposición de la marca (publicidad, registro, posicionamiento).</li>
        <li><strong>Método de mercado:</strong> comparación con transacciones similares de marcas en el mismo sector.</li>
        <li><strong>Método de royalties relevados:</strong> capitalización de las regalías que pagaría un tercero por usar la marca.</li>
    </ul>
    <p>La valuación debe ser realizada por un profesional idóneo y documentada adecuadamente para tener validez legal y fiscal.</p>
